				<p>1、高清机顶盒的安装方法</p>
                <p>HDMI接法：</p>
                <p>第一步：连接机顶盒的白色RF信号线，有旋钮一头钮接在机顶盒螺旋接头上，另一头连接用户家有线电视用户盒，如用户家无用户盒，可直接连接有线电视信号接入口。</p>
                <p>第二步：高清机顶盒有个HDMI接口（与USB接口形状相同），同时插入机顶盒的HDMI接口和电视机的HDMI接口即可。</p>
                <P>第三步：插入智能卡，打开机顶盒电源和电视电源必须把电视机的图像模式改成HDMI模式收看</P>
                <br/>
                <p>2、家庭影院的观看方法？</p>
                <p>答：遥控器上影院快捷键-进入家庭影院出现节目列表有六个节目（国际影院、华语影院、上周精选、热播剧场、海外剧场、双休连播），选择一个节目按确认进入该节目，上下键选择一个片名按确定进入，上下选择节目名称，光标移至右边显示在播的条目确定即可收看。</p>
                <br/>
                <p>3、如何使用遥控器的“学习”功能？</p>
                <p>答：设置键长按2秒后会长亮，再按被学习的键后，会闪烁，再按要学习的键会快闪三下后长亮，再按设置会熄灭即学习成功，整个学习过程需在10秒之内完成。</p>
                <br/>
                <p>4、为什么会显示“无法识别卡”或“请插卡”现象？</p>
                <p>答：出现以上故障提示可能是机顶盒卡槽松动，或卡与机顶盒接触不好，可请用户重新插拨卡，如仍无效可建议用户用一张普通白纸擦拭芯片后再插入机顶盒试试。如仍不能恢复，需请用户带上机顶盒、智能卡、到公司检测点进行检测。（检测网点设在营业厅内：孺子路营业厅、南京东路营业厅、昌北营业厅、下罗营业厅）</p>
                <br/>
                <p>5、为什么安装机顶盒后会出现卡顿或马赛克的现象？</p>
                <p>答：机顶盒虽然具有一定的纠错能力，但如果遇到网络故障或各种强干扰，就会引起信号质量差，产生大量的误码，超过了机身的纠错能力，所能解决的问题范围，从电视机的画面上表现出来，就是卡顿或马赛克现象。建议用户拧紧信号线，如仍有问题可致电96130客服热线报修。</p>
                <br/>
                <p>6、客户电脑无法上网怎么办？</p>
                <p>答：处理步骤：第一步：把路由器（如果安装了路由器）关闭电源，等待5分钟后再打开是否能上网，如不能上网见下一步；第二步：把路由器先拆掉，把路由器的进线从路由器上拔下，直接插在电脑网卡上，然后点击桌面宽带连接看是否能上网。如果能上了，说明路由器出故障；如果不能上网，在进行宽带连接时会出现一个三位数的错误代码，根据错误代码判断故障出现位置。（请参考以下代码排查故障）</p>
                <p>●错误691故障：不能通过验证，可能的原因：用户的账户或者密码输入错误，或用户的账户余额不足，用户在使用时未正常退出而造成用户账号驻留 。解决方法：确认用户账号和密码是否正确，等待几分钟或重新启动后再拨号，如果仍无法解决可联系维护人员处理。</p>
                <p>●错误769故障：无法拨号，没有合适的网卡和驱动，可能原因：网卡未安装好、网卡驱动不正常或网卡损坏，解决方法：检查网卡是否工作正常或更新网卡驱动。</p>
                <p>●错误678故障：无法建立连接</p>
                <p>可能原因：这个故障比较复杂，用户和BRAS链路中任何一个环节有问题，都可能导致678故障。解决方法：应根据不同的情况作相应处理，最好联系维护人员处理。</p>
